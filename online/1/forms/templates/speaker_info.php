		<p>1996 - Крафт Якобс Сушард, Австрия. Специализация: маркетинг</p>
		<p>1997 - Крафт Якобс Сушард, Австрия. Специализация: управление складами</p>
		<p>1999 - Крафт Якобс Сушард, Австрия. Специализация: менеджмент</p>
		<p>1998 - Кодак, Германия - Великобритания. Специализация: стратегический маркетинг и менеджмент</p>
